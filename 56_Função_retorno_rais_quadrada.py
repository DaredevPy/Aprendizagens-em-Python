
def raiz (numero):
    import math
    resultado = math.sqrt(numero)
    return (resultado)
valor_da_raiz = raiz(81)
print(valor_da_raiz)

# O codigo acima é um exemplo de função que retorna um valor. A função raiz recebe um numero e retorna a raiz quadrada desse numero.
# A função math.sqrt() é usada para calcular a raiz quadrada de um numero. O valor retornado pela função é armazenado na variavel valor_da_raiz.